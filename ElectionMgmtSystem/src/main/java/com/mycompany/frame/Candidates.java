    private void ctableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ctableMouseClicked
        // TODO add your handling code here:
        try {
            
            DefaultTableModel model = (DefaultTableModel)ctable.getModel();
            int id,k;
            k = ctable.getSelectedRow();
            id = Integer.valueOf(model.getValueAt(k, 0).toString());
            coid = id;
            String na,ag,photo,eid,ge,sql = "Select * from candidateinfo where cid = ?";
            na = ag = photo = eid = ge = null;
            try {
               ps = con.prepareStatement(sql);
               ps.setInt(1, id);
               
               rs = ps.executeQuery();
               
               if(rs.next()){
                   na = rs.getString(2);
                   ag = rs.getInt(3)+"";
                   ge = rs.getString(4);
                   eid = rs.getInt(6)+"";
                   
                   name.setText(na);
                   age.setText(ag);
                   if(ge.equals("Male")) male.setSelected(true);
                   else if(ge.equals("Female")) female.setSelected(true);
                   cphoto.setIcon(ResizePhoto(null, rs.getBytes(5)));
                   etype.setSelectedItem(eid);
               }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
                e.printStackTrace();
            }
            coid = id;
            
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_ctableMouseClicked

