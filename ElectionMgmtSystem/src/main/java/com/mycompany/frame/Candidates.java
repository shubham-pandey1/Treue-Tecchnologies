    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String na,gen, sql = "update candidateinfo set name = ?, age = ?, gender = ?, elect = ?  where cid = ?";
        na = gen = null;
        int ag,el;
        ag = el = 0;
        na = name.getText();
        ag = Integer.parseInt( age.getText());
        el = Integer.parseInt( etype.getSelectedItem()+"");
        if(male.isSelected()) gen = "Male";
        else if(female.isSelected()) gen = "female";
        if(coid == -1 || ag == 0 || na == null || el<1 || gen == null){
            JOptionPane.showMessageDialog(this, "Please provide all Details");
        }
        else{
            
            try {
                ps = con.prepareStatement(sql);
                
                ps.setString(1, na);
                ps.setInt(2, ag);
                ps.setString(3, gen);
                ps.setInt(4, el);
                ps.setInt(5, coid);
                
                int j = ps.executeUpdate();
                if(imgpath != null){
                    sql = "update candidateinfo set photo = ? where cid = ?";
                    try {
                        ps = con.prepareStatement(sql);
                         try {
                            InputStream img = new FileInputStream(imgpath);
                            ps.setBlob(1, img);
                         } catch (FileNotFoundException ex) {
                            Logger.getLogger(Candidates.class.getName()).log(Level.SEVERE, null, ex);
                        }
                         ps.setInt(2, coid);
                         ps.executeUpdate();
                        
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "There is error in Image upload");
                        e.printStackTrace();
                    }
                }
                JOptionPane.showMessageDialog(null, "Candidate detail updated successfully");
                cTable();
                clr();
                imgpath = null;
                coid = -1;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

