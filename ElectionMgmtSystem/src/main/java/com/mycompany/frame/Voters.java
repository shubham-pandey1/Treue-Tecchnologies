    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        if(vid == -1) JOptionPane.showMessageDialog(null, "Select which Voter to be deleted");
        else{
            int input = JOptionPane.showConfirmDialog(null, "Do you want to Delete?", "Confirm...",
				JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
            if (input == 0){
            try {
            ps = con.prepareStatement("delete from voter where vid = ?");
            ps.setInt(1, vid);
            int i = ps.executeUpdate();
            if(i>0)
            JOptionPane.showMessageDialog(null, "Voter detail deleted Successfully");
            vTable();
            clr();
            vid = -1;
            } catch (SQLException ex) {
                Logger.getLogger(Election.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex);
            }
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        String na,gen,pass, sql = "update voter set name = ?, age = ?, gender = ?,pass = ?, velection = ? where vid = ?";
        na = gen = pass  = null;
        int ag,el;
        ag = el = 0;
        na = name.getText();
        ag = Integer.parseInt( age.getText());
        el = Integer.parseInt( election.getSelectedItem()+"");
        pass = new String(pwd.getPassword());
        if(male.isSelected()) gen = "Male";
        else if(female.isSelected()) gen = "Female";
        if(vid == -1 || ag < 1 || na == null || el<1 || gen == null || pass == null){
            JOptionPane.showMessageDialog(this, "Please provide all Details");
        }
        else{
            
            try {
                ps = con.prepareStatement(sql);
                
                ps.setString(1, na);
                ps.setInt(2, ag);
                ps.setString(3, gen);
                ps.setString(4, pass);
                ps.setInt(5, el);
                ps.setInt(6, vid);
                
                int j = ps.executeUpdate();
                if(j>0)
                    JOptionPane.showMessageDialog(null, "Voter detail updated successfully");
                vTable();
                clr();
                
                vid = -1;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_editActionPerformed

