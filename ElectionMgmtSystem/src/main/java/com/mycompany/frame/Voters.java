    private void vtableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_vtableMouseClicked
        // TODO add your handling code here:
        try {
            
            DefaultTableModel model = (DefaultTableModel)vtable.getModel();
            int id,k;
            k = vtable.getSelectedRow();
            id = Integer.valueOf(model.getValueAt(k, 0).toString());
            vid = id;
            String na,ag,pass,eid,ge,sql = "Select * from voter where vid = ?";
            na = ag = pass = eid = ge = null;
            try {
               ps = con.prepareStatement(sql);
               ps.setInt(1, id);
               
               rs = ps.executeQuery();
               
               if(rs.next()){
                   name.setText(rs.getString(2));
                   age.setText(rs.getInt(3)+"");
                   pwd.setText(rs.getString(4));
                   ge = rs.getString(5);
                   eid = rs.getInt(6)+"";
                   
                   if(ge.equals("Male")) male.setSelected(true);
                   else if(ge.equals("Female")) female.setSelected(true);
                   
                   
                   election.setSelectedItem(eid);
               }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
                e.printStackTrace();
            }
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_vtableMouseClicked

